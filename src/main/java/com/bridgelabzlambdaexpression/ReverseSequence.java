package com.bridgelabzlambdaexpression;

public class ReverseSequence {
    public static void main(String[] args) {
        String str = "Java is a Backend Language";  //
        String words[] = str.split("\\s+");
        for(int i=words.length-1;i>=0;i--);
        {
            System.out.print();
        }
    }
    void show();
}
